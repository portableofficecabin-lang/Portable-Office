export const getProductsByCategory = (categorySlug: string): Product[] => {
  return products.filter((p) => p.categorySlug === categorySlug);
};

export const getFeaturedProducts = (): Product[] => {
  return products.filter((p) => p.featured);
};

export const getProductById = (id: string): Product | undefined => {
  return products.find((p) => p.id === id);
};

export const getProductSlug = (product: Product): string => {
  const slug = product.name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '');
  const hasDuplicate = products.filter(p => p.name === product.name).length > 1;
  return hasDuplicate ? `${slug}-${product.id}` : slug;
};

export const getProductDetailPath = (product: Product): string => {
  return `/products/${getProductSlug(product)}.html`;
};

export const getProductBySlug = (slug: string): Product | undefined => {
  const normalizedSlug = slug.replace(/\.html$/i, "");
  const legacySlugMap: Record<string, string> = {
    "shipping-container": "14",
    "shipping-container-for-sale": "14",
    "cargo-storage-container-shipping-container": "12",
    "cargo-container-for-sale": "17",
  };

  return products.find((p) => getProductSlug(p) === normalizedSlug || p.id === legacySlugMap[normalizedSlug]);
};
